#def  nome da função  (valor da funcao01, valor da funcao02 )
def media (n1, n2):
    #define oque seria o resultado da funcao.
    result = (n1 + n2) / 2
    #return result retorna o o resultado armazenada na variavel result
    return result 
#resultado da funcao media dos valores entre ()
r = media (10,2)
print (r)
